public class frequency_of_most_frequent_element {
    public int maxFrequency(int[] nums, int k) {
        Map<Integer,Integer> M = new HashMap();

        for(int n : nums){
            if (M.containsKey(n)) {
                M.put(n, M.get(n)+1);
            }else{
                M.put(n,1);
            }
        }


    }
}
